"""Evaluate the segmenter on the held-out test split and write the report.

`make eval` runs this. It loads `data/test.npz` (regenerating it if needed),
runs the committed `segmenter.onnx` through onnxruntime, and computes mean IoU +
per-class IoU + a confusion matrix via `eval.metrics`. Running the committed ONNX
model (rather than a torch `.pt` checkpoint, which is regenerated by `make train`
and not committed) is what lets the headline regenerate from a clean checkout with
no torch and no GPU. `make train` reuses this same path to evaluate the model it
just exported, so `make train` and `make eval` report identical numbers.
"""
from __future__ import annotations

import argparse

import numpy as np

from .. import config
from ..data import generate
from . import metrics as M
from . import report


def _load_test_split():
    path = config.DATA_DIR / "test.npz"
    if not path.exists():
        generate.main()
    d = np.load(path)
    return d["images"], d["masks"]


def predict(model, images: np.ndarray, batch_size: int = config.BATCH_SIZE) -> np.ndarray:
    """Run the torch model over images [N,1,H,W], return predicted masks [N,H,W]."""
    import torch

    model.eval()
    preds = []
    with torch.no_grad():
        for i in range(0, len(images), batch_size):
            x = torch.from_numpy(images[i : i + batch_size])
            logits = model(x)
            preds.append(logits.argmax(dim=1).cpu().numpy())
    return np.concatenate(preds, axis=0)


def load_model(checkpoint=None):
    import torch

    from ..model.unet import UNetLite

    if checkpoint is None:
        checkpoint = config.CHECKPOINT
    model = UNetLite()
    state = torch.load(checkpoint, map_location="cpu")
    model.load_state_dict(state["model_state"] if "model_state" in state else state)
    return model


def predict_onnx(images: np.ndarray, model_path=None, batch_size: int = config.BATCH_SIZE) -> np.ndarray:
    """Run the committed ONNX segmenter over images [N,1,H,W], return masks [N,H,W].

    Uses onnxruntime on ``segmenter.onnx`` (the committed artifact), so eval needs
    neither torch nor the uncommitted ``segmenter.pt`` and reproduces on a clean clone.
    """
    import onnxruntime as ort

    if model_path is None:
        model_path = config.ONNX_MODEL
    session = ort.InferenceSession(str(model_path), providers=["CPUExecutionProvider"])
    preds = []
    for i in range(0, len(images), batch_size):
        batch = np.ascontiguousarray(images[i : i + batch_size], dtype=np.float32)
        logits = session.run(None, {"frame": batch})[0]
        preds.append(logits.argmax(axis=1))
    return np.concatenate(preds, axis=0)


def onnx_num_parameters(model_path=None) -> int:
    """Learnable-parameter count of the committed ONNX model.

    Sums the graph initializer sizes, excluding any BatchNorm running mean/var
    buffers (non-learnable) that the exporter keeps separate. For the exported
    UNet-lite, whose BatchNorm layers are folded into the preceding convolutions,
    this is the parameter count of the model that is actually served and evaluated.
    """
    import onnx

    if model_path is None:
        model_path = config.ONNX_MODEL
    graph = onnx.load(str(model_path)).graph
    buffers: set[str] = set()
    for node in graph.node:
        if node.op_type == "BatchNormalization" and len(node.input) >= 5:
            buffers.update(node.input[3:5])  # input_mean, input_var
    return int(
        sum(
            (int(np.prod(init.dims)) if init.dims else 1)
            for init in graph.initializer
            if init.name not in buffers
        )
    )


def evaluate(model=None, append_history: bool = True) -> dict:
    """Evaluate the committed ONNX model, or a passed-in torch ``model``, and write reports.

    With ``model=None`` (the `make eval` path) this runs the committed
    ``segmenter.onnx`` through onnxruntime. `make train` passes the freshly trained
    torch model so it evaluates exactly what it just fit; both routes write the same
    report format.
    """
    images, masks = _load_test_split()
    if model is None:
        pred = predict_onnx(images)
        num_parameters = onnx_num_parameters()
    else:
        pred = predict(model, images)
        num_parameters = model.num_parameters()
    metrics = M.summarize(pred, masks)
    meta = {
        "n_train": config.N_TRAIN,
        "n_test": len(images),
        "frame_size": config.FRAME_SIZE,
        "seed": config.SEED,
        "epochs": config.EPOCHS,
        "batch_size": config.BATCH_SIZE,
        "lr": config.LR,
        "num_parameters": num_parameters,
    }
    report.write_reports(metrics, meta, append_history=append_history)
    return metrics


def main() -> None:
    ap = argparse.ArgumentParser(description="Evaluate the defect segmenter on the held-out split.")
    ap.add_argument("--no-history", action="store_true", help="do not append to history.jsonl")
    args = ap.parse_args()
    metrics = evaluate(append_history=not args.no_history)
    print(f"mean IoU (defect classes): {metrics['mean_iou']:.4f}")
    for name, v in metrics["per_class_iou"].items():
        print(f"  {name:18s}: {'n/a' if v is None else f'{v:.4f}'}")
    print(f"wrote {config.rel(config.EVAL_MD)}")


if __name__ == "__main__":
    main()
