"""Render the eval report (artifacts/EVAL.md) and append to history.jsonl.

Everything written here comes from a real evaluation of a trained checkpoint on
the held-out split. No number is hand-entered.
"""
from __future__ import annotations

import json
from datetime import datetime, timezone

from .. import config, schema


def _fmt(v: float | None) -> str:
    return "n/a" if v is None else f"{v:.4f}"


def render_markdown(metrics: dict, meta: dict) -> str:
    pc = metrics["per_class_iou"]
    cm = metrics["confusion_matrix"]
    names = metrics["class_names"]

    lines: list[str] = []
    lines.append("# Defect-Segmentation Eval")
    lines.append("")
    lines.append(
        "Generated by `python -m constellation_vision.train` (which trains then "
        "evaluates) or `python -m constellation_vision.eval.harness` (eval only). "
        "The test split is held out by generation seed in `data/test.npz`; no "
        "frame seed is shared with the train split. Ground truth is the synthetic "
        "mask from `data/generate.py` under `schema.CLASSES`."
    )
    lines.append("")
    lines.append("## Headline")
    lines.append("")
    lines.append("| Metric | Value |")
    lines.append("|---|---|")
    lines.append(f"| Mean IoU (defect classes) | **{_fmt(metrics['mean_iou'])}** |")
    lines.append(f"| Mean IoU (incl. background) | {_fmt(metrics['mean_iou_with_background'])} |")
    lines.append(f"| Pixel accuracy | {_fmt(metrics['pixel_accuracy'])} |")
    lines.append("")
    lines.append(
        f"Train frames: {meta['n_train']}, held-out test frames: {meta['n_test']}, "
        f"frame size: {meta['frame_size']}x{meta['frame_size']}, "
        f"model parameters: {meta['num_parameters']:,}."
    )
    lines.append("")
    lines.append("## Per-class IoU")
    lines.append("")
    lines.append("| Class | IoU |")
    lines.append("|---|---|")
    for name in names:
        lines.append(f"| {name} | {_fmt(pc[name])} |")
    lines.append("")
    lines.append("## Confusion matrix (rows = ground truth, cols = prediction, pixel counts)")
    lines.append("")
    header = "| gt \\ pred | " + " | ".join(names) + " |"
    lines.append(header)
    lines.append("|" + "---|" * (len(names) + 1))
    for i, name in enumerate(names):
        row = " | ".join(str(v) for v in cm[i])
        lines.append(f"| {name} | {row} |")
    lines.append("")
    lines.append(
        f"_Run: seed {meta['seed']}, {meta['epochs']} epochs, batch {meta['batch_size']}, "
        f"lr {meta['lr']}. Generated {meta['generated_at']}._"
    )
    lines.append("")
    return "\n".join(lines)


def write_reports(metrics: dict, meta: dict, append_history: bool = True) -> None:
    config.artifacts_dir()
    meta = dict(meta)
    meta.setdefault("generated_at", datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ"))

    config.EVAL_MD.write_text(render_markdown(metrics, meta), encoding="utf-8")

    payload = {
        "mean_iou": metrics["mean_iou"],
        "mean_iou_with_background": metrics["mean_iou_with_background"],
        "pixel_accuracy": metrics["pixel_accuracy"],
        "per_class_iou": metrics["per_class_iou"],
        "meta": meta,
    }
    config.METRICS_JSON.write_text(json.dumps(payload, indent=2) + "\n", encoding="utf-8")

    if append_history:
        row = {
            "generated_at": meta["generated_at"],
            "mean_iou": metrics["mean_iou"],
            "pixel_accuracy": metrics["pixel_accuracy"],
            "n_train": meta["n_train"],
            "n_test": meta["n_test"],
            "epochs": meta["epochs"],
        }
        with open(config.HISTORY, "a", encoding="utf-8") as fh:
            fh.write(json.dumps(row) + "\n")


def load_committed_mean_iou() -> float | None:
    """The mean IoU recorded in the committed metrics.json, if present."""
    if not config.METRICS_JSON.exists():
        return None
    data = json.loads(config.METRICS_JSON.read_text(encoding="utf-8"))
    return float(data["mean_iou"])
